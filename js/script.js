function วิ่งเปอร์เซ็นต์(องค์ประกอบ, เปอร์เซ็นต์สูงสุด=100, ระยะเวลา=2000) {
  let หลอด = องค์ประกอบ.querySelector('.โบนัสเปอร์เซ็นต์');
  if (!หลอด) return;
  let ค่าปัจจุบัน = 0;
  const เพิ่มต่อรอบ = เปอร์เซ็นต์สูงสุด / (ระยะเวลา / 30);
  const interval = setInterval(() => {
    ค่าปัจจุบัน += เพิ่มต่อรอบ;
    if (ค่าปัจจุบัน >= เปอร์เซ็นต์สูงสุด) {
      ค่าปัจจุบัน = เปอร์เซ็นต์สูงสุด;
      clearInterval(interval);
    }
    หลอด.style.width = ค่าปัจจุบัน + '%';
  }, 30);
}

